// Generate per-glyph SVG outline paths for the hero's italic line so it can
// be stroke-drawn like handwriting. Re-run if the headline text changes:
//   node scripts/gen-headline-paths.mjs
import * as fontkit from "fontkit";
import { writeFileSync } from "node:fs";

const TEXT = "that run themselves.";
// match the live CSS: h1 .ital is Fraunces italic, font-weight 420, and at
// hero sizes the opsz axis rides at/near its 144 max
const AXES = { wght: 420, opsz: 144 };
const OUT = "components/headlinePaths.ts";

const vf = fontkit.openSync("scripts/.fonts/Fraunces-Italic-VF.ttf");
const font = vf.getVariation(AXES);
const upm = font.unitsPerEm;

const run = font.layout(TEXT);
let x = 0;
const glyphs = [];
const bbox = { minX: Infinity, minY: Infinity, maxX: -Infinity, maxY: -Infinity };
for (let i = 0; i < run.glyphs.length; i++) {
  const g = run.glyphs[i];
  const pos = run.positions[i];
  if (g.path.commands.length) {
    const gx = x + pos.xOffset;
    const gy = pos.yOffset;
    const b = g.path.bbox; // capture before transform (may mutate the path)
    // bake position + y-flip into the path data so the SVG needs no
    // transform attributes (the print stylesheet nukes transforms globally)
    const d = g.path.transform(1, 0, 0, -1, gx, -gy).toSVG();
    glyphs.push({ d });
    bbox.minX = Math.min(bbox.minX, b.minX + gx);
    bbox.minY = Math.min(bbox.minY, b.minY + gy);
    bbox.maxX = Math.max(bbox.maxX, b.maxX + gx);
    bbox.maxY = Math.max(bbox.maxY, b.maxY + gy);
  }
  x += pos.xAdvance;
}

const data = {
  text: TEXT,
  upm,
  ascent: font.ascent,
  descent: font.descent, // negative
  width: x,
  bbox, // tight ink bounds of the whole run, font units (y-up)
  glyphs,
};

writeFileSync(
  OUT,
  `// Generated by scripts/gen-headline-paths.mjs — do not edit by hand.
// "${TEXT}" in Fraunces Italic (wght ${AXES.wght}, opsz ${AXES.opsz}).
// Path data is y-down with glyph positions baked in; bbox is y-up font units.
export interface HeadlineGlyph { d: string }
export interface HeadlineBBox { minX: number; minY: number; maxX: number; maxY: number }
export interface HeadlineData {
  text: string; upm: number; ascent: number; descent: number;
  width: number; bbox: HeadlineBBox; glyphs: HeadlineGlyph[];
}
const data: HeadlineData = ${JSON.stringify(data)};
export default data;
`
);
console.log(
  `wrote ${OUT}: ${glyphs.length} glyphs, width ${x}u, upm ${upm}, ascent ${font.ascent}, descent ${font.descent}`
);
